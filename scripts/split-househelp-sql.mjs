/**
 * Tách househelp.sql thành schema (DDL) và seed (DML) cho Docker init.
 * Chạy: node scripts/split-househelp-sql.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const src = path.join(root, 'househelp.sql');
const outDir = path.join(root, 'database', 'docker-init');

const lines = fs.readFileSync(src, 'utf8').split(/\r?\n/);

// Các khoảng [start, end) — chỉ số dòng 0-based trong househelp.sql
const schemaRanges = [
  [8, 21],
  [21, 328],
  [475, 490],
  [557, 598],
  [599, 613],
  [978, 994],
  [1004, 1017],
];

const schemaHeader = [
  '-- Generated by scripts/split-househelp-sql.mjs — không sửa tay; chạy lại script sau khi đổi househelp.sql',
  'SET NAMES utf8mb4;',
  'SET CHARACTER SET utf8mb4;',
  "SET SQL_MODE = 'NO_AUTO_VALUE_ON_ZERO';",
  'SET time_zone = "+00:00";',
  'SET FOREIGN_KEY_CHECKS=0;',
  '',
];

const schemaFooter = ['', 'SET FOREIGN_KEY_CHECKS=1;'];

const seedRanges = [
  [329, 475],
  [490, 557],
  [614, 976],
  [995, 1003],
  [1017, lines.length],
];

const seedHeader = [
  '-- Generated by scripts/split-househelp-sql.mjs',
  'SET NAMES utf8mb4;',
  'SET CHARACTER SET utf8mb4;',
  "SET SQL_MODE = 'NO_AUTO_VALUE_ON_ZERO';",
  'SET time_zone = "+00:00";',
  'START TRANSACTION;',
  '',
];

const seedFooter = [''];

function sliceRanges(ranges) {
  const chunks = [];
  for (const [a, b] of ranges) {
    chunks.push(lines.slice(a, b).join('\n'));
  }
  return chunks.join('\n\n');
}

fs.mkdirSync(outDir, { recursive: true });

const schemaBody = sliceRanges(schemaRanges);
fs.writeFileSync(
  path.join(outDir, '01_schema.sql'),
  [...schemaHeader, schemaBody, ...schemaFooter].join('\n'),
  'utf8'
);

const seedBody = sliceRanges(seedRanges);
fs.writeFileSync(
  path.join(outDir, '02_seed.sql'),
  [...seedHeader, seedBody, ...seedFooter].join('\n'),
  'utf8'
);

console.log('Wrote', path.join(outDir, '01_schema.sql'));
console.log('Wrote', path.join(outDir, '02_seed.sql'));
